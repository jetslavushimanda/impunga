export function filterFundingSources({ age, sex, occupation, businessStage, sector, province }) {
  return FUNDING_SOURCES.filter(source => {
    const eligibility = source.eligibility;
    if (age) {
      if (eligibility.minAge && age < eligibility.minAge) return false;
      if (eligibility.maxAge && age > eligibility.maxAge) return false;
    }
    if (sex && eligibility.sex !== 'any' && eligibility.sex !== sex) return false;
    if (occupation && !eligibility.occupation.includes(occupation)) return false;
    if (businessStage && !eligibility.businessStage.includes(businessStage)) return false;
    if (sector && eligibility.sector !== 'all' && !eligibility.sector.includes(sector)) return false;
    if (province && eligibility.province !== 'all' && !eligibility.province.includes(province)) return false;
    return true;
  });
}
